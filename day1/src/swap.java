public class swap {
    public static void main(String args[]){
        int first=10,second=12;
        System.out.println("Before Swap");
        System.out.println("First number = "+first);
        System.out.println("Second number = "+second);

        int temp = first;
        first = second;
        second = temp;

        System.out.println("Before Swap");
        System.out.println("First number = "+first);
        System.out.println("Second number = "+second);


    }
}
